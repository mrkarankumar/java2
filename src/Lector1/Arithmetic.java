package Lector1;

public class Arithmetic {

    public static void main(String[] args) {
        int a = 72;
        int b = 66;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        double x = 34;
        double y = 23;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

    }
}
